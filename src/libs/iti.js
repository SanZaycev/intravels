    initialize: function({ setPhoneCode, setPhone, phone_code, phone }){
        if (!this.state.activated){
            this._setupState({ setPhoneCode, setPhone, phone_code });
            this.rows.box = document.getElementById('iti');
            this.rows.phone = document.getElementById('phone');
            if (this.rows.box && this.rows.phone){
                this.rows.selected_box = this.rows.box.children[0];
                this.rows.list = this.rows.box.children[1];
                this._buildList();
                this._setupListeners(phone);
                this.state.activated = true;
            }
        }
    },
    reload: function(props){
        if (typeof props === "object"){
            this.state.selected_code = props.phone_code ? props.phone_code : 7;
            if (this.rows.phone && this.rows.selected_box){
                const numbers = this._getNumbersValue(this.rows.phone.value);
                this.rows.phone.value = this._getFormattedValue(numbers);
                for (let i in this.data){
                    let c = this.data[i];
                    if (c[2] === this.state.selected_code){
                        this.rows.selected_box.children[0].className = 'iti__flag iti__' + c[1];
                        this.rows.selected_box.children[2].innerText = "+" + c[2];
                        this.rows.phone.style.paddingLeft = this.rows.selected_box.getBoundingClientRect().width + "px";
                        break;
                    }
                }
            }
        }
    },
    _setupState({ setPhoneCode, setPhone, phone_code }){
        if (setPhoneCode){ this.callbacks.setPhoneCode = setPhoneCode; }
        if (setPhone){ this.callbacks.setPhone = setPhone; }
        this.state.selected_code = phone_code ?? 7;
    },
    _buildList: function(){
        let c = 0;
        let has_code = false;
        for (let i in this.data){
            let country = this.data[i];
            if (!has_code && JSON.parse(this.rows.selected_box.dataset.code) === country[2]){
                has_code = true;
                this.rows.selected_box.children[0].className = 'iti__flag iti__' + country[1];
                this.callbacks.setPhoneCode(country[2]);
            }
            let item = document.createElement('li');
            item.className = 'iti-item';
            item.id = 'iti-' + country[1];
            item.role = 'option';
            item.dataset.code = country[2];
            item.innerHTML = '<div class="iti-flag-box"><div class="iti__flag iti__'+ country[1] +'"></div></div><span class="iti-country-name">'+ country[0] +'</span><span class="iti-text-code">+'+ country[2] +'</span>';
            this.rows.list.append(item);
            c++;
            if (c === 12){
                let separator = document.createElement('li');
                separator.className = 'iti-divider';
                this.rows.list.append(separator);
            }
            this._listItemListener(item, country);
        }
    },
    _listItemListener: function(item, country){
        item.addEventListener('click', function(){ ITI._onListItemClick(country); });
    },
    _setupListeners: function(phone){
        if (this.rows.selected_box){
            this.rows.phone.style.paddingLeft = this.rows.selected_box.getBoundingClientRect().width + "px";
            this.rows.selected_box.addEventListener('click', function(e){ ITI._onSelectedBoxClick(e); });
        }
        if (this.rows.phone){
            this.rows.phone.value = this._getFormattedValue(this._getNumbersValue(phone));
            this.rows.phone.addEventListener("input", this._onPhoneInput, false);
            this.rows.phone.addEventListener("paste", this._onPhonePaste, false);
            this.rows.phone.addEventListener('keydown', this._onPhoneKeyDown, false);
            this._checkNumListener(this.rows.phone);
        }
    },
    _onPhonePaste: function(e){
        let input = e.target;
        let pasted = e.clipboardData || window.clipboardData;
        let numbers = ITI._getNumbersValue(input.value);
        if (pasted){
            const text = pasted.getData("Text");
            if (/\D/g.test(text)){
                ITI.callbacks.setPhone(numbers);
                input.value = ITI._getFormattedValue(numbers);
            }
        }
    },
    _onPhoneInput: function(e){
        let input = e.target;
        let numbers = ITI._getNumbersValue(input.value);
        let start = input.selectionStart;
        if (!numbers){
            input.value = "";
            ITI.callbacks.setPhone(null);
            return;
        }
        if (ITI.state.keyCode === 8){ return }

        ITI.callbacks.setPhone(numbers);
        input.value = ITI._getFormattedValue(numbers);

        if (input.value.length === start){ return }
        if ('selectionStart' in input){
            switch (start){
                case 1:
                    start = 2;
                break;
                case 5:
                    start = 7;
                break;
                case 6:
                    start = 7;
                break;
                case 10:
                    start = 11;
                break;
                case 13:
                    start = 14;
                break;
                default: break;
            }
            input.selectionStart = start;
            input.selectionEnd = start;
        }
    },
    _onPhoneKeyDown: function(e){
        ITI.state.keyCode = e.keyCode;
    },
    _getNumbersValue: function(value){
        return value.trim().replace(/\D/g, "");
    },
    _getFormattedValue: function(numbers){
        let v = "";
        if (numbers.length > 0){
            v += '(' + numbers.substring(0, 3);
        }
        if (numbers.length >= 4){
            v += ') ' + numbers.substring(3, 6);
        }
        if (numbers.length >= 7){
            v += '-' + numbers.substring(6, 8);
        }
        if (numbers.length >= 9){
            v += '-' + numbers.substring(8, 10);
        }
        return v
    },
    _checkNumListener: function(input){
        input.onkeypress = function(e){
            if (e.charCode && (e.charCode < 48 || e.charCode > 57 || e.keyCode === 8)){ return false; }
        };
    },
    _onListItemClick: function(country){
        let flag = document.createElement('div');
        flag.className = "iti__flag iti__" + country[1];
        this.rows.selected_box.title = country[0] + " +" + country[2];
        this.rows.selected_box.children[0].remove();
        this.rows.selected_box.prepend(flag);
        this.rows.list.classList.remove('open');
        this.rows.selected_box.children[1].classList.remove('up');
        this.rows.selected_box.children[2].innerText = "+" + country[2];
        document.removeEventListener('click', this._open, false);
        this.state.selected_code = country[2];
        this.rows.selected_box.dataset.code = country[2];
        this.rows.phone.style.paddingLeft = this.rows.selected_box.getBoundingClientRect().width + "px";
        this.callbacks.setPhoneCode(country[2]);
    },
    _onSelectedBoxClick: function(){
        this.rows.list.classList.toggle('open');
        this.rows.selected_box.children[1].classList.toggle('up');
        if (this.rows.list.classList.contains('open')){
            document.addEventListener('click', this._open, false);
        } else {
            document.removeEventListener('click', this._open, false);
        }
    },
    _open: function(event){
        const target = event.target;
        if (!ITI.rows.selected_box.contains(target) && !ITI.rows.list.contains(target) && ITI.rows.list.classList.contains('open')){
            ITI.rows.list.classList.remove('open');
            ITI.rows.selected_box.children[1].classList.remove('up');
            document.removeEventListener('click', ITI._open, false);
        }
    },
    destroy: function (){
        this.rows = {
            box: null,
            phone: null,
            list: null,
            selected_box: null
        };
        this.state = {
            view: null,
            selected_code: null,
            keyCode: false,
            activated: false
        };
    }
